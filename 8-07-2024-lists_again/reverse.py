# Развернуть список в обратном порядке не создавая для этого новый список

# [1,3,2,5,6] --> [6,5,2,3,1]
# [1,3,2,5,6] -->  [6,3,2,5,1] --> [1,5,2,3,6]

# [1,2,3,4,5,6] --> [6,2,3,4,5,1] --> [6,5,3,4,2,1] --> [6,5,4,3,2,1]

lst = [1,2,3,4,5,6]

for i in range(len(lst)//2):
    c = lst[i]
    lst[i] = lst[len(lst)-1-i]
    lst[len(lst)-1-i] = c

print(lst)