a = 7 # это тип данных целые числа
b = 4 # Для них определены все арифметические операции

res = a + b # сумма
print(res)

res = a - b #разность
print(res)

res = a * b # умножение
print(res)

res = a / b # деление
print(res)

res = a // b # целочисленное деление
print(res)

res = a % b # остаток от деления
print(res)

res = a ** b # возведение в степень
print(res)


