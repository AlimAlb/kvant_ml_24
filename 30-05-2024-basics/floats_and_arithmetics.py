a = 10.234 # а это тип данных вещественных чисел или чисел с дробной частью
b = 2.251 # для них определены все те же операции

res = a + b # сумма
print(res)

res = a - b #разность
print(res)

res = a * b # умножение
print(res)

res = a / b # деление
print(res)

res = a // b # целочисленное деление
print(res)

res = a % b # остаток от деления
print(res)

res = a ** b # возведение в степень
print(res)

