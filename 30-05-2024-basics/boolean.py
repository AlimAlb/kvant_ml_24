# Логический тип данных представляет собой еще один тип в питоне
# Он состоит всего из двух значений: True и False
# Для работы с ним определены три оператора: and, or и not

# and
res = True and True
print(res)
res = True and False
print(res)
res = False and True
print(res)
res = False and False
print(res)

# or
res = True or True
print(res)
res = True or False
print(res)
res = False or True
print(res)
res = False or False
print(res)

# not
res = not(True)
print(res)
res = not(False)
print(res)


