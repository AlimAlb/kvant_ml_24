# Также в питоне присутсвуют математические операторы, которые
# в результате своего исполнения возвращают логические значения True или False

a = 5 
b = 3

res = a > b # больше
print(res) 
res = a >= b # больше либо равно
print(res)
res = a < b # меньше
print(res)
res = a <= b # меньше либо равно
print(res)
res = a == b # равно
print(res)
res = a != b # не равно
print(res)



